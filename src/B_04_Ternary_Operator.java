public class B_04_Ternary_Operator {
    public static void main(String[] args) {
        // Ternary Operator ( ?: )
        // condition ? true : false

        int n1 = 10;
        int n2 = 20;

        int greaterNum = (n2 > n1) ? n2 : n1;
        System.out.println(greaterNum);
        
    }
}
